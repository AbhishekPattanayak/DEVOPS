#Creating class

class student:  #Class keyword to define a class
    name = "Abhishek"
    name2 = "Alpita"

#Creating object (Inside the class we are creating a object)

s1 = student()  # Objects are created from the above class
s2 = student()
print(s1.name)
print(s2.name2)

